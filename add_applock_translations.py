import os
import xml.etree.ElementTree as ET

def add_translations_to_file(lang_code, translations):
    """Add translations to a specific language strings.xml file"""
    base_path = r"E:\Dropbox\GIT\paperless client\app\src\main\res"

    if lang_code == "de":
        file_path = os.path.join(base_path, "values", "strings.xml")
    else:
        file_path = os.path.join(base_path, f"values-{lang_code}", "strings.xml")

    if not os.path.exists(file_path):
        print(f"File not found: {file_path}")
        return

    # Read the file
    with open(file_path, 'r', encoding='utf-8') as f:
        content = f.read()

    # Check if translations already exist
    if "app_lock_title" in content:
        print(f"Translations already exist in {lang_code}, skipping...")
        return

    # Find the closing </resources> tag
    if "</resources>" not in content:
        print(f"Invalid XML structure in {lang_code}")
        return

    # Build translation block
    translation_block = "\n    <!-- App-Lock -->\n"
    for key, value in translations.items():
        translation_block += f'    <string name="{key}">{value}</string>\n'

    # Insert before </resources>
    content = content.replace("</resources>", translation_block + "</resources>")

    # Write back
    with open(file_path, 'w', encoding='utf-8') as f:
        f.write(content)

    print(f"[OK] Added translations to {lang_code}")

